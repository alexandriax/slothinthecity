def mesh_stats(meshes: Sequence[bpy.types.Object]) -> Dict[str, int]:
    for obj in meshes:
        obj.data.calc_loop_triangles()
    return {
        "meshes": len(meshes),
        "vertices": sum(len(obj.data.vertices) for obj in meshes),
        "triangles": sum(len(obj.data.loop_triangles) for obj in meshes),
        "materials": len({slot.material.name for obj in meshes for slot in obj.material_slots if slot.material}),
    }


def file_sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for block in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(block)
    return digest.hexdigest()


def glb_json(path: Path) -> Dict:
    with path.open("rb") as handle:
        magic, version, length = struct.unpack("<4sII", handle.read(12))
        if magic != b"glTF" or version != 2:
            raise RuntimeError(f"{path} is not glTF 2.0")
        while handle.tell() < length:
            chunk_length, chunk_type = struct.unpack("<II", handle.read(8))
            data = handle.read(chunk_length)
            if chunk_type == 0x4E4F534A:
                return json.loads(data.decode("utf-8"))
    raise RuntimeError(f"{path} does not contain a JSON chunk")


def verify_glb(path: Path) -> Dict[str, object]:
    data = glb_json(path)
    animations = [animation.get("name") for animation in data.get("animations", [])]
    missing = [name for name in CLIP_NAMES if name not in animations]
    if missing:
        raise RuntimeError(f"{path.name} is missing authored clips: {missing}; found {animations}")
    if not data.get("skins"):
        raise RuntimeError(f"{path.name} has no skin")
    if not any(primitive.get("attributes", {}).get("JOINTS_0") is not None for mesh in data.get("meshes", []) for primitive in mesh.get("primitives", [])):
        raise RuntimeError(f"{path.name} has no JOINTS_0 attribute")
    if not any(material.get("normalTexture") for material in data.get("materials", [])):
        raise RuntimeError(f"{path.name} has no normal-mapped material")
    triangles = 0
    vertices = 0
    for mesh in data.get("meshes", []):
        for primitive in mesh.get("primitives", []):
            triangles += data["accessors"][primitive["indices"]]["count"] // 3
            vertices += data["accessors"][primitive["attributes"]["POSITION"]]["count"]
    return {
        "animations": animations,
        "skins": len(data.get("skins", [])),
        "nodes": len(data.get("nodes", [])),
        "embeddedImages": len(data.get("images", [])),
        "triangles": triangles,
        "vertices": vertices,
    }


