ASSET_ID = "gary-polar-bear"
CLIP_NAMES = ("BearIdle", "BearWalk", "BearForage", "BearTurn")
TEXTURE_SIZE = 2048


def parse_args() -> argparse.Namespace:
    argv = sys.argv[sys.argv.index("--") + 1 :] if "--" in sys.argv else []
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output", required=True, type=Path)
    parser.add_argument("--source", type=Path, default=Path(__file__).resolve().parent / "source")
    parser.add_argument("--preview", type=Path)
    parser.add_argument("--keep-blend", action="store_true")
    parser.add_argument("--texture-size", type=int, default=TEXTURE_SIZE)
    parser.add_argument("--voxel-size", type=float, default=0.023)
    return parser.parse_args(argv)


def ensure_object_mode() -> None:
    if bpy.context.object and bpy.context.object.mode != "OBJECT":
        bpy.ops.object.mode_set(mode="OBJECT")


def select_only(objects: Iterable[bpy.types.Object]) -> None:
    ensure_object_mode()
    bpy.ops.object.select_all(action="DESELECT")
    active = None
    for obj in objects:
        obj.hide_set(False)
        obj.hide_viewport = False
        obj.hide_render = False
        obj.select_set(True)
        active = obj
    if active is not None:
        bpy.context.view_layer.objects.active = active


def purge_scene() -> None:
    ensure_object_mode()
    bpy.ops.object.select_all(action="SELECT")
    bpy.ops.object.delete(use_global=False)
    for datablocks in (
        bpy.data.meshes,
        bpy.data.curves,
        bpy.data.armatures,
        bpy.data.materials,
        bpy.data.images,
        bpy.data.actions,
    ):
        for block in list(datablocks):
            if block.users == 0:
                datablocks.remove(block)


def apply_modifiers(obj: bpy.types.Object) -> None:
    select_only([obj])
    bpy.context.view_layer.objects.active = obj
    for modifier in list(obj.modifiers):
        bpy.ops.object.modifier_apply(modifier=modifier.name)


def add_volume(
    name: str,
    location: Sequence[float],
    scale: Sequence[float],
    rotation: Sequence[float] = (0.0, 0.0, 0.0),
    subdivisions: int = 2,
) -> bpy.types.Object:
    """Add a sculpt volume; every volume is fused before it can ship."""
    bpy.ops.mesh.primitive_ico_sphere_add(subdivisions=subdivisions, radius=1.0, location=location)
    obj = bpy.context.object
    obj.name = f"SculptVolume.{name}"
    obj.scale = scale
    obj.rotation_euler = rotation
    select_only([obj])
    bpy.ops.object.transform_apply(location=False, rotation=True, scale=True)
    return obj


def make_body(voxel_size: float) -> bpy.types.Object:
    """Create a single watertight polar-bear skin from an anatomical blockout.

    The volumes overlap only as a sculpting aid.  Voxel remeshing replaces all
    of their surfaces with one new manifold surface, so the exported body has
    no intersecting shells, floating paws, or sphere seams.
    """
    volumes: List[bpy.types.Object] = []
    add = lambda name, loc, scale, rot=(0.0, 0.0, 0.0), sub=2: volumes.append(
        add_volume(name, loc, scale, rot, sub)
    )

    # Long, powerful but not obese ursine trunk. +Y is the nose direction in
    # Blender and becomes -Z (THREE forward) in the exported y-up glTF.
    add("RibCage", (0.0, 0.02, 1.40), (0.69, 1.47, 0.59), (math.radians(-3), 0, 0), 3)
    add("ShoulderGirdle", (0.0, 0.58, 1.50), (0.70, 0.84, 0.62), (math.radians(-6), 0, 0), 3)
    add("Pelvis", (0.0, -0.86, 1.35), (0.63, 0.82, 0.53), (math.radians(4), 0, 0), 3)
    add("AbdomenTaper", (0.0, -0.32, 1.31), (0.63, 0.91, 0.49), (0, 0, 0), 2)
    add("Withers", (0.0, 0.40, 1.72), (0.52, 0.72, 0.38), (math.radians(-8), 0, 0), 2)

    # Neck and head form a low, tapered polar-bear profile, not a round teddy
    # head. The muzzle is substantially longer than a brown bear's.
    add("LowerNeck", (0.0, 1.15, 1.54), (0.54, 0.84, 0.51), (math.radians(-14), 0, 0), 3)
    add("UpperNeck", (0.0, 1.52, 1.61), (0.46, 0.76, 0.42), (math.radians(-12), 0, 0), 3)
    add("Cranium", (0.0, 1.78, 1.69), (0.43, 0.50, 0.38), (math.radians(-7), 0, 0), 3)
    add("CheekL", (0.22, 1.99, 1.61), (0.24, 0.36, 0.25), (0, math.radians(-8), 0), 2)
    add("CheekR", (-0.22, 1.99, 1.61), (0.24, 0.36, 0.25), (0, math.radians(8), 0), 2)
    add("MuzzleBridge", (0.0, 2.20, 1.60), (0.27, 0.48, 0.21), (math.radians(-8), 0, 0), 3)
    add("MuzzlePad", (0.0, 2.53, 1.54), (0.29, 0.32, 0.21), (math.radians(-10), 0, 0), 2)
    add("Chin", (0.0, 2.34, 1.41), (0.275, 0.43, 0.16), (math.radians(-7), 0, 0), 2)
    # Small, rounded ears embedded deep in the coat.
    add("EarL", (0.40, 1.53, 1.83), (0.065, 0.050, 0.060), (math.radians(8), 0, math.radians(-20)), 2)
    add("EarR", (-0.40, 1.53, 1.83), (0.065, 0.050, 0.060), (math.radians(8), 0, math.radians(20)), 2)

    # Forelegs drop almost straight beneath the shoulders.  Multiple tapered
    # volumes describe scapula, upper limb, wrist and broad five-toed paw.
    for side_name, x in (("L", 0.53), ("R", -0.53)):
        add(f"Scapula{side_name}", (x, 0.67, 1.36), (0.28, 0.36, 0.44), (0, math.radians(3 if x > 0 else -3), 0), 2)
        add(f"ForeUpper{side_name}", (x, 0.74, 0.95), (0.23, 0.25, 0.46), (math.radians(-2), 0, 0), 2)
        add(f"ForeWrist{side_name}", (x, 0.78, 0.49), (0.155, 0.18, 0.34), (math.radians(-1), 0, 0), 2)
        add(f"ForeCarpal{side_name}", (x, 0.84, 0.25), (0.145, 0.17, 0.15), (math.radians(-8), 0, 0), 2)
        paw_rotation = math.radians(5 if x > 0 else -5)
        add(f"ForeKnuckle{side_name}", (x - math.copysign(.02, x), 1.04, 0.16), (0.205, 0.23, 0.13), (math.radians(-7), 0, paw_rotation), 2)
        add(f"ForePaw{side_name}", (x - math.copysign(.02, x), 1.03, 0.09), (0.205, 0.34, 0.09), (math.radians(-4), 0, paw_rotation), 2)
        for digit, offset in enumerate((-.095, -.032, .032, .095)):
            add(
                f"ForeToeWeb{side_name}{digit}",
                (x - math.copysign(.02, x) + offset, 1.27, .075),
                (.059, .13, .065),
                (math.radians(-6), 0, paw_rotation),
                2,
            )

    # Hind legs retain the characteristic powerful thigh and plantigrade hock.
    for side_name, x in (("L", 0.51), ("R", -0.51)):
        add(f"Thigh{side_name}", (x, -0.78, 1.14), (0.34, 0.45, 0.52), (math.radians(5), 0, 0), 2)
        add(f"HindKnee{side_name}", (x, -0.84, 0.78), (0.25, 0.30, 0.34), (math.radians(4), 0, 0), 2)
        add(f"HindHock{side_name}", (x, -0.67, 0.44), (0.17, 0.29, 0.29), (math.radians(-14), 0, 0), 2)
        add(f"HindAnkle{side_name}", (x, -0.48, 0.24), (0.17, 0.22, 0.145), (math.radians(-16), 0, 0), 2)
        paw_rotation = math.radians(4 if x > 0 else -4)
        add(f"HindKnuckle{side_name}", (x - math.copysign(.015, x), -0.18, 0.16), (0.21, 0.25, 0.13), (math.radians(-8), 0, paw_rotation), 2)
        add(f"HindPaw{side_name}", (x - math.copysign(.015, x), -0.22, 0.09), (0.215, 0.38, 0.09), (math.radians(-5), 0, paw_rotation), 2)
        for digit, offset in enumerate((-.10, -.034, .034, .10)):
            add(
                f"HindToeWeb{side_name}{digit}",
                (x - math.copysign(.015, x) + offset, .07, .075),
                (.063, .13, .065),
                (math.radians(-7), 0, paw_rotation),
                2,
            )

    add("TailBase", (0.0, -1.41, 1.30), (0.22, 0.28, 0.24), (math.radians(-18), 0, 0), 2)

    # One manifold surface replaces every blockout volume.
    select_only(volumes)
    bpy.context.view_layer.objects.active = volumes[0]
    bpy.ops.object.join()
    body = bpy.context.object
    body.name = "Body"
    body.data.name = "GaryContinuousAnatomicalSkin"
    body.data.remesh_voxel_size = voxel_size
    body.data.remesh_voxel_adaptivity = 0.0
    select_only([body])
    bpy.ops.object.voxel_remesh()

    # Relax voxel stair-stepping while retaining shoulder, hock and muzzle
    # planes. A restrained multi-octave displacement gives the silhouette the
    # irregularity of dense guard fur without ballooning the anatomy.
    smooth = body.modifiers.new("Anatomical surface relaxation", "SMOOTH")
    smooth.factor = 0.48
    smooth.iterations = 12
    apply_modifiers(body)

    refine_anatomical_planes(body)

    fur_texture = bpy.data.textures.new("GaryGuardHairMicroSculpt", type="CLOUDS")
    fur_texture.noise_scale = 0.035
    fur_texture.noise_depth = 2
    displacement = body.modifiers.new("Guard hair micro silhouette", "DISPLACE")
    displacement.texture = fur_texture
    displacement.strength = 0.0028
    displacement.texture_coords = "GLOBAL"
    displacement.mid_level = 0.56
    apply_modifiers(body)

    # Give all four paws a shared planted contact plane. Preserve the rounded
    # toe walls above it, but remove the rocking-chair underside that makes a
    # heavy quadruped look weightless on level habitat ground.
    inverse = body.matrix_world.inverted()
    for vertex in body.data.vertices:
        world = body.matrix_world @ vertex.co
        if world.z < .052:
            world.z = .026
            vertex.co = inverse @ world
        elif world.z < .095:
            blend = (world.z - .052) / (.095 - .052)
            world.z = .026 * (1.0 - blend) + world.z * blend
            vertex.co = inverse @ world
    body.data.update()

    for polygon in body.data.polygons:
        polygon.use_smooth = True
    body["continuous_anatomical_mesh"] = True
    body["source_geometry"] = "original-repository-authored"
    return body


def refine_anatomical_planes(body: bpy.types.Object) -> None:
    """Hand-shape the fused skin into adult polar-bear planes.

    The voxel union guarantees one continuous surface. This deterministic
    vertex pass supplies the concave orbit, brow/cheek breaks, long nasal
    taper, jaw plane and limb-joint pinches that overlapping ellipsoids cannot
    describe on their own. It is symmetric, non-destructive to topology, and
    uses no external model or sampled geometry.
    """
    inverse = body.matrix_world.inverted()

    def gaussian(value: float, center: float, radius: float) -> float:
        return math.exp(-((value - center) / radius) ** 2)

    for vertex in body.data.vertices:
        point = body.matrix_world @ vertex.co
        x, y, z = point
        side = abs(x)
        sign = -1.0 if x < 0 else 1.0

        # A low crown and held brow replace the domed teddy-bear forehead.
        crown = gaussian(y, 1.78, .43) * max(0.0, min(1.0, (z - 1.74) / .26))
        point.z -= crown * .045

        if side > .12:
            orbit = gaussian(y, 2.07, .19) * gaussian(z, 1.68, .14)
            brow = gaussian(y, 1.99, .20) * gaussian(z, 1.80, .075)
            cheek = gaussian(y, 2.12, .23) * gaussian(z, 1.50, .12)
            jaw = gaussian(y, 2.39, .34) * gaussian(z, 1.34, .10)
            facial_delta = -orbit * .042 + brow * .018 + cheek * .030 + jaw * .012
            point.x = sign * max(.015, side + facial_delta)

        # Recess the eye opening in the forward-facing skull surface, then
        # hold a narrow upper lid/brow and a subtler lower lid around it. The
        # separate dark eye insert sits almost flush behind these skin planes.
        if y > 2.02:
            eye_center_x = math.copysign(.30, x)
            eye_width = gaussian(x, eye_center_x, .115)
            face_front = max(0.0, min(1.0, (y - 2.02) / .23))
            socket = gaussian(z, 1.675, .085)
            upper_lid = gaussian(z, 1.705, .035)
            lower_lid = gaussian(z, 1.645, .030)
            brow_plane = gaussian(z, 1.755, .040)
            point.y += eye_width * face_front * (
                -socket * .024
                + upper_lid * .018
                + lower_lid * .010
                + brow_plane * .014
            )

        # Narrow the bony bridge before the soft muzzle expands distally.
        bridge = gaussian(y, 2.28, .30) * gaussian(z, 1.61, .27)
        distal_release = max(0.0, min(1.0, (y - 2.48) / .28))
        if y > 2.02:
            taper = 1.0 - bridge * .13 * (1.0 - distal_release)
            point.x *= taper

        # Separate the lower jaw/chin plane from the upper muzzle bulb.
        chin = gaussian(y, 2.38, .36) * max(0.0, min(1.0, (1.49 - z) / .20))
        point.z -= chin * .025

        # Preserve identifiable elbow/carpal and knee/hock changes rather than
        # leaving constant-width columns. Each field pinches only the local
        # transverse section and leaves the continuous joins intact.
        for limb_x in (-.53, .53):
            if abs(x - limb_x) < .34:
                fore_y = gaussian(y, .78, .30)
                fore_joint = fore_y * (
                    gaussian(z, .80, .13) * .055
                    + gaussian(z, .38, .11) * .075
                )
                hind_y = gaussian(y, -.72, .36)
                hind_joint = hind_y * (
                    gaussian(z, .76, .14) * .045
                    + gaussian(z, .43, .11) * .070
                )
                pinch = max(fore_joint, hind_joint)
                if pinch > 0:
                    local_x = point.x - limb_x
                    point.x = limb_x + local_x * (1.0 - pinch)

        # Shallow dorsal channels separate the four visible toe webs without
        # cutting through the paw or creating detachable digits. The sole and
        # interdigital base remain one load-bearing manifold surface.
        paw_center = math.copysign(.51, point.x) if abs(point.x) > .24 else 0.0
        local_paw_x = point.x - paw_center
        if paw_center and abs(local_paw_x) < .19 and point.z > .10:
            toe_field = max(gaussian(y, 1.27, .15), gaussian(y, .07, .15))
            groove = max(gaussian(local_paw_x, offset, .016) for offset in (-.07, 0.0, .07))
            dorsal = max(0.0, min(1.0, (point.z - .10) / .075))
            point.z -= toe_field * groove * dorsal * .014

        vertex.co = inverse @ point
    body.data.update()


def smart_uv(obj: bpy.types.Object) -> None:
    select_only([obj])
    bpy.context.view_layer.objects.active = obj
    bpy.ops.object.mode_set(mode="EDIT")
    bpy.ops.mesh.select_all(action="SELECT")
    # Keep broad torso/head/limb planes in larger coherent islands. The old
    # 52-degree split fragmented the deforming flank into many tiny islands,
    # making tangent-normal boundaries visible during the walk cycle.
    bpy.ops.uv.smart_project(angle_limit=math.radians(82), island_margin=0.008)
    bpy.ops.object.mode_set(mode="OBJECT")


def write_texture_set(output: Path, size: int) -> Dict[str, Path]:
    """Create original fur PBR maps; no source pixels or model assets used."""
    output.mkdir(parents=True, exist_ok=True)
    rng = np.random.default_rng(93479021)
    u = np.linspace(0.0, 1.0, size, endpoint=False, dtype=np.float32)
    v = np.linspace(0.0, 1.0, size, endpoint=False, dtype=np.float32)
    xx, yy = np.meshgrid(u, v)

    # Dense short-fur breakup: filtered micro-noise supplies individual guard
    # hair clusters, while many low-frequency directions create non-repeating
    # undercoat variation. A previous four-sine version produced long grooves;
    # this deliberately has no dominant continuous stripe direction.
    fine = rng.normal(0.0, 1.0, (size, size)).astype(np.float32)
    for _ in range(3):
        fine = (
            fine * 2.0
            + np.roll(fine, 1, 0) + np.roll(fine, -1, 0)
            + np.roll(fine, 1, 1) + np.roll(fine, -1, 1)
        ) / 6.0
    fine /= max(float(fine.std()), 1e-6)
    micro = rng.normal(0.0, 1.0, (size, size)).astype(np.float32)
    micro = (micro * 2.0 + np.roll(micro, 1, 0) + np.roll(micro, -1, 0) + np.roll(micro, 1, 1) + np.roll(micro, -1, 1)) / 6.0
    micro /= max(float(micro.std()), 1e-6)
    broad = np.zeros((size, size), dtype=np.float32)
    for _ in range(18):
        angle = float(rng.uniform(0, math.tau))
        frequency = float(rng.uniform(1.8, 11.0))
        phase = float(rng.uniform(0, math.tau))
        broad += np.sin((xx * math.cos(angle) + yy * math.sin(angle)) * math.tau * frequency + phase)
    broad /= max(float(broad.std()), 1e-6)
    height = np.clip(fine * .48 + micro * .20 + broad * .10, -1.0, 1.0)

    base = np.zeros((size, size, 4), dtype=np.float32)
    warmth = np.clip(.5 + broad * .10, 0, 1)
    luminosity = np.clip(.78 + height * .055 + broad * .025, .62, .92)
    base[..., 0] = np.clip(luminosity * (1.02 + warmth * .018), 0, 1)
    base[..., 1] = np.clip(luminosity * (1.00 + warmth * .006), 0, 1)
    base[..., 2] = np.clip(luminosity * (.91 - warmth * .018), 0, 1)
    base[..., 3] = 1.0

    # Tangent-space normals from the same directional height field.
    grad_y, grad_x = np.gradient(height)
    strength = 1.35
    nx = -grad_x * strength
    ny = -grad_y * strength
    nz = np.ones_like(nx)
    norm = np.sqrt(nx * nx + ny * ny + nz * nz)
    normal = np.stack((nx / norm * .5 + .5, ny / norm * .5 + .5, nz / norm * .5 + .5, np.ones_like(nx)), axis=-1)

    roughness_value = np.clip(.80 + height * .055 + broad * .04, .68, .94)
    roughness = np.stack((roughness_value, roughness_value, roughness_value, np.ones_like(height)), axis=-1)

    paths = {
        "albedo": output / f"{ASSET_ID}-albedo.png",
        "normal": output / f"{ASSET_ID}-normal.png",
        "roughness": output / f"{ASSET_ID}-roughness.png",
    }
    for kind, pixels in (("albedo", base), ("normal", normal), ("roughness", roughness)):
        image = bpy.data.images.new(f"GaryFur{kind.title()}", width=size, height=size, alpha=True, float_buffer=False)
        # Blender image pixels are bottom-up; symmetry makes the flip harmless,
        # but preserve conventional texture orientation anyway.
        image.pixels.foreach_set(np.flipud(pixels).reshape(-1))
        image.filepath_raw = str(paths[kind])
        image.file_format = "PNG"
        image.save()
        bpy.data.images.remove(image)
    return paths


def make_runtime_texture_set(source_paths: Dict[str, Path]) -> Dict[str, Path]:
    """Transcode source PNGs to high-quality embedded web delivery images."""
    runtime_dir = Path(tempfile.gettempdir()) / "sloth-gary-runtime-textures"
    runtime_dir.mkdir(parents=True, exist_ok=True)
    runtime_paths: Dict[str, Path] = {}
    for kind, source in source_paths.items():
        image = bpy.data.images.load(str(source), check_existing=False)
        # Force lazy disk pixels resident before changing the destination path.
        _ = image.pixels[0]
        # glTF repacks metallic/roughness channels into a PNG. Downsample only
        # this low-frequency delivery map to avoid a multi-megabyte packed PNG;
        # the editable 2K roughness source remains lossless above.
        if kind == "roughness":
            image.scale(1024, 1024)
        destination = runtime_dir / f"{ASSET_ID}-{kind}.{'png' if kind == 'roughness' else 'jpg'}"
        image.filepath_raw = str(destination)
        image.file_format = "PNG" if kind == "roughness" else "JPEG"
        image.save()
        runtime_paths[kind] = destination
        bpy.data.images.remove(image)
    return runtime_paths


def relink_fur_material(material: bpy.types.Material, texture_paths: Dict[str, Path]) -> None:
    names = {
        "albedo": "Gary original fur albedo",
        "normal": "Gary original guard-hair normal",
        "roughness": "Gary original fur roughness",
    }
    for kind, node_name in names.items():
        node = material.node_tree.nodes[node_name]
        old = node.image
        node.image = bpy.data.images.load(str(texture_paths[kind]), check_existing=True)
        if kind != "albedo":
            node.image.colorspace_settings.name = "Non-Color"
        if old and old.users == 0:
            bpy.data.images.remove(old)


def principled_node(material: bpy.types.Material):
    return material.node_tree.nodes.get("Principled BSDF")


def make_fur_material(texture_paths: Dict[str, Path]) -> bpy.types.Material:
    material = bpy.data.materials.new("GaryPolarBearFur")
    material.use_nodes = True
    nodes = material.node_tree.nodes
    links = material.node_tree.links
    principled = principled_node(material)
    principled.inputs["Base Color"].default_value = (0.82, 0.81, 0.73, 1.0)
    principled.inputs["Roughness"].default_value = 0.84
    if "Sheen" in principled.inputs:
        principled.inputs["Sheen"].default_value = 0.16

    albedo_node = nodes.new("ShaderNodeTexImage")
    albedo_node.name = "Gary original fur albedo"
    albedo_node.image = bpy.data.images.load(str(texture_paths["albedo"]))
    albedo_node.image.colorspace_settings.name = "sRGB"
    links.new(albedo_node.outputs["Color"], principled.inputs["Base Color"])

    rough_node = nodes.new("ShaderNodeTexImage")
    rough_node.name = "Gary original fur roughness"
    rough_node.image = bpy.data.images.load(str(texture_paths["roughness"]))
    rough_node.image.colorspace_settings.name = "Non-Color"
    links.new(rough_node.outputs["Color"], principled.inputs["Roughness"])

    normal_node = nodes.new("ShaderNodeTexImage")
    normal_node.name = "Gary original guard-hair normal"
    normal_node.image = bpy.data.images.load(str(texture_paths["normal"]))
    normal_node.image.colorspace_settings.name = "Non-Color"
    normal_map = nodes.new("ShaderNodeNormalMap")
    # Geometry already carries a guard-hair micro-sculpt. The map supplies a
    # restrained secondary response without overpowering anatomy or exposing
    # UV tangent transitions under animation.
    normal_map.inputs["Strength"].default_value = 0.12
    links.new(normal_node.outputs["Color"], normal_map.inputs["Color"])
    links.new(normal_map.outputs["Normal"], principled.inputs["Normal"])
    return material


def simple_material(name: str, color: Sequence[float], roughness: float, metallic: float = 0.0) -> bpy.types.Material:
    material = bpy.data.materials.new(name)
    material.use_nodes = True
    material.diffuse_color = tuple(color)
    node = principled_node(material)
    node.inputs["Base Color"].default_value = tuple(color)
    node.inputs["Roughness"].default_value = roughness
    node.inputs["Metallic"].default_value = metallic
