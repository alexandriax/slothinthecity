    lod0_path = args.output / f"{ASSET_ID}-lod0.glb"
    lod2_path = args.output / f"{ASSET_ID}-lod2.glb"
    gltf_export(lod0_path, [*meshes, rig])
    gltf_export(lod2_path, [*lod2_meshes, lod2_rig])

