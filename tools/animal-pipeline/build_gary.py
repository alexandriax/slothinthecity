def duplicate_lod(meshes: Sequence[bpy.types.Object], rig: bpy.types.Object, ratio: float) -> Tuple[List[bpy.types.Object], bpy.types.Object]:
    select_only(list(meshes) + [rig])
    bpy.ops.object.duplicate()
    duplicates = list(bpy.context.selected_objects)
    lod_rig = next(obj for obj in duplicates if obj.type == "ARMATURE")
    lod_rig.name = "GaryRig.LOD2"
    lod_meshes = [obj for obj in duplicates if obj.type == "MESH"]
    for obj in lod_meshes:
        obj.name = f"{obj.name}.LOD2"
        obj.data.name = "GaryContinuousAnatomicalSkinLOD2" if obj.name.startswith("Body") else f"{obj.name.removesuffix('.LOD2')}LOD2"
        obj.parent = lod_rig
        for modifier in list(obj.modifiers):
            if modifier.type == "ARMATURE":
                obj.modifiers.remove(modifier)
        if len(obj.data.polygons) > 1000:
            modifier = obj.modifiers.new("Silhouette-aware LOD2 decimation", "DECIMATE")
            modifier.decimate_type = "COLLAPSE"
            modifier.ratio = ratio
            modifier.use_collapse_triangulate = True
            modifier.use_symmetry = True
            apply_modifiers(obj)
            limit_vertex_influences(obj, 4)
        armature = obj.modifiers.new("GaryQuadrupedRig", "ARMATURE")
        armature.object = lod_rig
    return lod_meshes, lod_rig


def limit_vertex_influences(obj: bpy.types.Object, limit: int) -> None:
    """Normalize decimator-interpolated weights to the glTF four-joint cap."""
    for vertex in obj.data.vertices:
        assignments = sorted(
            ((item.group, item.weight) for item in vertex.groups if item.weight > 1e-8),
            key=lambda item: item[1],
            reverse=True,
        )
        if not assignments:
            continue
        kept = assignments[:limit]
        removed = assignments[limit:]
        for group_index, _ in removed:
            obj.vertex_groups[group_index].remove([vertex.index])
        total = sum(weight for _, weight in kept)
        for group_index, weight in kept:
            obj.vertex_groups[group_index].add([vertex.index], weight / total, "REPLACE")


def triangulate(meshes: Sequence[bpy.types.Object]) -> None:
    for obj in meshes:
        # Triangulate the source mesh without applying the armature modifier.
        # Applying the whole modifier stack would bake the rest pose and strip
        # JOINTS_0/WEIGHTS_0 from the exported asset.
        mesh = bmesh.new()
        mesh.from_mesh(obj.data)
        bmesh.ops.triangulate(mesh, faces=list(mesh.faces))
        mesh.to_mesh(obj.data)
        mesh.free()
        obj.data.update()
        for polygon in obj.data.polygons:
            polygon.use_smooth = True


