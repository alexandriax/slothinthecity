#!/usr/bin/env python3
"""Build the original, browser-ready Gary polar-bear assets.

This is an authored Blender pipeline, not a runtime assembly of spheres.  The
script fuses a deliberately blocked anatomical sculpt, voxel-remeshes it into
one watertight skin, refines the silhouette, UV unwraps the result, binds it to
an explicit quadruped skeleton, authors four actions, and exports two GLBs.

Run with Blender (3.4 or newer):

  /Applications/Blender.app/Contents/MacOS/Blender \
    --background --factory-startup \
    --python tools/animal-pipeline/build_gary.py -- \
    --output public/game/animals/authored \
    --preview /tmp/sloth-animal-previews

All source geometry, textures, weights and animation curves are generated by
this repository script. Wildlife photographs may be consulted for proportion
reference, but no third-party meshes or texture pixels enter the output.
"""

