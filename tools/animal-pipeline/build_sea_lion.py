def point_segment_distance(point: Vector, start: Vector, end: Vector) -> float:
    delta = end - start
    if delta.length_squared < 1e-12:
        return (point - start).length
    t = max(0, min(1, (point - start).dot(delta) / delta.length_squared))
    return (point - (start + delta * t)).length


def skin_object(obj: bpy.types.Object, rig: bpy.types.Object, rigid_bone: str | None = None) -> None:
    for group in list(obj.vertex_groups):
        obj.vertex_groups.remove(group)
    groups = {bone.name: obj.vertex_groups.new(name=bone.name) for bone in rig.data.bones}
    if rigid_bone:
        groups[rigid_bone].add([v.index for v in obj.data.vertices], 1, "REPLACE")
    else:
        axis = tuple(
            (name, compact_haul_out_y(y))
            for name, y in (
                ("TailBase", -1.18), ("Pelvis", -.72), ("Spine", -.15),
                ("Chest", .37), ("NeckLower", .70), ("NeckUpper", 1.00),
                ("Head", 1.30),
            )
        )

        def smoothstep(value: float) -> float:
            value = max(0.0, min(1.0, value))
            return value * value * (3 - 2 * value)

        def assign(vertex_index: int, assignments: Dict[str, float]) -> None:
            total = sum(max(weight, 0) for weight in assignments.values())
            for name, weight in assignments.items():
                if weight > .0001:
                    groups[name].add([vertex_index], weight / total, "REPLACE")

        for vertex in obj.data.vertices:
            p = vertex.co
            lateral = abs(p.x)
            axial_weights: Dict[str, float] = {}
            if p.y <= axis[0][1]:
                axial_weights[axis[0][0]] = 1
            elif p.y >= axis[-1][1]:
                axial_weights[axis[-1][0]] = 1
            else:
                for (left_name, left_y), (right_name, right_y) in zip(axis, axis[1:]):
                    if left_y <= p.y <= right_y:
                        blend = smoothstep((p.y - left_y) / (right_y - left_y))
                        axial_weights = {left_name: 1 - blend, right_name: blend}
                        break

            # Continuous shoulder-to-web field.  The axial weights fade out
            # over a broad girdle instead of switching at a geometric test,
            # keeping every edge on both sides of the root within the same
            # four-influence envelope.
            side = "L" if p.x >= 0 else "R"
            fore_lateral = smoothstep((lateral - .17) / .34)
            fore_lower = smoothstep((.83 - p.z) / .28)
            fore_front = smoothstep((p.y + .08) / .18)
            # The fan sweeps forward to y≈.62.  Saturate the appendage field
            # before that distal edge so it transforms as one hydrofoil;
            # fading back into Chest at the tip was the source of the former
            # foldover and triangle inversions in Swim/Dive.
            fore_back = smoothstep((.82 - p.y) / .14)
            fore_gate = fore_lateral * fore_lower * min(fore_front, fore_back)
            assignments = {
                name: weight * (1 - fore_gate)
                for name, weight in axial_weights.items()
            }
            # The approved broad fan behaves as a single hydrofoil.  Keeping
            # the web on ForeFlipper avoids candy-wrapper collapse from linear
            # blending between differently oriented root and tip bones.  The
            # stable ForeTip bones and animation channels remain in the public
            # pinniped-v1 interface for runtime compatibility.
            assignments[f"ForeFlipper.{side}"] = assignments.get(f"ForeFlipper.{side}", 0) + fore_gate

            # The paired rear webs share TailBase smoothly through the midline;
            # bilateral hind influences only take over once lateral separation
            # is established, eliminating the former center-seam tearing.
            hind_rear = smoothstep((-p.y - .82) / .30)
            hind_lateral = smoothstep((lateral - .008) / .13)
            hind_gate = hind_rear * hind_lateral
            if hind_gate > 0:
                assignments = {
                    name: weight * (1 - hind_gate)
                    for name, weight in assignments.items()
                }
                hind_tip = smoothstep((-p.y - 1.39) / .46)
                assignments[f"HindFlipper.{side}"] = assignments.get(f"HindFlipper.{side}", 0) + hind_gate * (1 - hind_tip)
                assignments[f"HindTip.{side}"] = assignments.get(f"HindTip.{side}", 0) + hind_gate * hind_tip
            assign(vertex.index, assignments)
    modifier = obj.modifiers.new("CaliforniaSeaLionRig", "ARMATURE")
    modifier.object = rig
    obj.parent = rig
    obj.matrix_parent_inverse = rig.matrix_world.inverted()


def reset_pose(rig: bpy.types.Object) -> None:
    for bone in rig.pose.bones:
        bone.rotation_mode = "XYZ"
        bone.location = (0, 0, 0)
        bone.rotation_euler = (0, 0, 0)
        bone.scale = (1, 1, 1)


def action_begin(rig: bpy.types.Object, name: str) -> bpy.types.Action:
    reset_pose(rig)
    action = bpy.data.actions.new(name)
    action.use_fake_user = True
    rig.animation_data_create()
    rig.animation_data.action = action
    return action


def key(rig, bone_name, frame, rotation=None, location=None, scale=None, group=""):
    bone = rig.pose.bones[bone_name]
    bone.rotation_mode = "XYZ"
    if rotation is not None:
        bone.rotation_euler = rotation
        bone.keyframe_insert("rotation_euler", frame=frame, group=group)
    if location is not None:
        bone.location = location
        bone.keyframe_insert("location", frame=frame, group=group)
    if scale is not None:
        bone.scale = scale
        bone.keyframe_insert("scale", frame=frame, group=group)


def finish_action(action: bpy.types.Action, interpolation="BEZIER") -> bpy.types.Action:
    for fcurve in action.fcurves:
        for point in fcurve.keyframe_points:
            point.interpolation = interpolation
    return action


def make_idle(rig) -> bpy.types.Action:
    name = "SeaLionIdle"
    action = action_begin(rig, name)
    for frame, breath, look in ((1, 0, 0), (30, .013, -.08), (60, 0, .05), (90, .011, .11), (120, 0, 0)):
        key(rig, "Root", frame, location=(0, 0, 0), group=name)
        key(rig, "Chest", frame, scale=(1 + breath * .4, 1 + breath * .5, 1 + breath), rotation=(0, 0, look * .13), group=name)
        key(rig, "NeckLower", frame, rotation=(-look * .08, 0, look * .32), group=name)
        key(rig, "NeckUpper", frame, rotation=(-look * .10, 0, look * .52), group=name)
        key(rig, "Head", frame, rotation=(look * .08, 0, look), group=name)
        for side, sign in (("L", 1), ("R", -1)):
            key(rig, f"ForeFlipper.{side}", frame, rotation=(0, 0, sign * breath * 1.5), group=name)
            key(rig, f"HindFlipper.{side}", frame, rotation=(0, 0, 0), group=name)
            key(rig, f"HindTip.{side}", frame, rotation=(0, 0, 0), group=name)
    return finish_action(action)


def make_swim(rig) -> bpy.types.Action:
    name = "SeaLionSwim"
    action = action_begin(rig, name)
    for frame, phase in ((1, 0), (10, math.pi / 2), (19, math.pi), (28, math.pi * 1.5), (37, math.tau)):
        stroke = math.sin(phase)
        recovery = math.cos(phase)
        key(rig, "Root", frame, location=(0, 0, math.sin(phase * 2) * .018), rotation=(math.radians(-3) + stroke * math.radians(2), 0, stroke * math.radians(2.5)), group=name)
        key(rig, "Spine", frame, rotation=(stroke * math.radians(3), 0, -stroke * math.radians(4)), group=name)
        key(rig, "Chest", frame, rotation=(-stroke * math.radians(2), 0, stroke * math.radians(3)), group=name)
        key(rig, "NeckLower", frame, rotation=(-stroke * math.radians(2), 0, -stroke * math.radians(2)), group=name)
        for side, sign in (("L", 1), ("R", -1)):
            # Strong foreflipper power stroke beneath the chest, followed by a
            # narrower recovery close to the body.
            # Keep the broad fan presented to the water throughout the cycle.
            # A former negative local-X twist turned the thin hydrofoil edge-on
            # and made the imported silhouette read as a severed paddle.
            fore_pitch = math.radians(7) + recovery * math.radians(5)
            key(rig, f"ForeFlipper.{side}", frame, rotation=(fore_pitch, sign * (math.radians(-3) + stroke * math.radians(7)), sign * (math.radians(2) + stroke * math.radians(12))), group=name)
            key(rig, f"ForeTip.{side}", frame, rotation=(-recovery * math.radians(6), sign * stroke * math.radians(4), sign * stroke * math.radians(4)), group=name)
            key(rig, f"HindFlipper.{side}", frame, rotation=(stroke * math.radians(2), 0, sign * stroke * math.radians(4)), group=name)
            key(rig, f"HindTip.{side}", frame, rotation=(-stroke * math.radians(2), 0, sign * stroke * math.radians(3)), group=name)
    return finish_action(action, "BEZIER")


