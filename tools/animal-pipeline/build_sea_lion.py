def glb_json(path: Path) -> Dict:
    with path.open("rb") as handle:
        magic, version, length = struct.unpack("<4sII", handle.read(12))
        if magic != b"glTF" or version != 2:
            raise RuntimeError(f"{path} is not glTF 2.0")
        while handle.tell() < length:
            chunk_length, chunk_type = struct.unpack("<II", handle.read(8))
            data = handle.read(chunk_length)
            if chunk_type == 0x4E4F534A:
                return json.loads(data.decode("utf-8"))
    raise RuntimeError(f"{path} has no JSON chunk")


def verify_glb(path: Path) -> Dict[str, object]:
    data = glb_json(path)
    clips = [animation.get("name") for animation in data.get("animations", [])]
    missing = [name for name in CLIP_NAMES if name not in clips]
    if missing:
        raise RuntimeError(f"{path.name} missing clips {missing}; found {clips}")
    if not data.get("skins"):
        raise RuntimeError(f"{path.name} has no skin")
    primitives = [primitive for mesh in data.get("meshes", []) for primitive in mesh.get("primitives", [])]
    if not any("JOINTS_0" in primitive.get("attributes", {}) and "WEIGHTS_0" in primitive.get("attributes", {}) for primitive in primitives):
        raise RuntimeError(f"{path.name} has no skinned primitive")
    if not any(material.get("normalTexture") for material in data.get("materials", [])):
        raise RuntimeError(f"{path.name} has no normal-mapped material")
    triangles = sum(data["accessors"][primitive["indices"]]["count"] // 3 for primitive in primitives)
    vertices = sum(data["accessors"][primitive["attributes"]["POSITION"]]["count"] for primitive in primitives)
    return {
        "animations": clips,
        "skins": len(data.get("skins", [])),
        "bones": max(len(skin.get("joints", [])) for skin in data.get("skins", [])),
        # Three GLTFLoader instantiates one runtime mesh per primitive. A
        # multi-material source mesh therefore counts more than once here.
        "meshes": len(primitives),
        "materials": len(data.get("materials", [])),
        "embeddedImages": len(data.get("images", [])),
        "triangles": triangles,
        "vertices": vertices,
    }


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for block in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(block)
    return digest.hexdigest()


def object_bounds(obj: bpy.types.Object) -> Tuple[Vector, Vector]:
    corners = [obj.matrix_world @ Vector(corner) for corner in obj.bound_box]
    return (
        Vector((min(p.x for p in corners), min(p.y for p in corners), min(p.z for p in corners))),
        Vector((max(p.x for p in corners), max(p.y for p in corners), max(p.z for p in corners))),
    )


